    def __init__(self, name, xp, level, gold, attack_power, defense, max_hp, hp, weapon, inventory):
        self.name = name
        self.xp = xp
        self.level = level
        self.gold = gold
        self.attack_power = attack_power
        self.defense = defense
        self.max_hp = max_hp
        self.hp = hp
        self.weapon = weapon
        self.inventory = inventory
    
        
    
